%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

    % ---- 注释 ---- %
        % ---- 本函数的主要作用是：
            %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
            % ---- 1.在指定的文件中写入100个“*”
            % ---- 2.换行
            %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

function write100Star(fid)
%% ---- 写入100个“*”
    for ii = 1:1:100
        fprintf(fid, '*');
    end
%% ---- 换行
    fprintf(fid, '\r\n');
end

